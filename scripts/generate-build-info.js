import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const PACKAGE_JSON_PATH = path.resolve(__dirname, '../package.json');
const OUTPUT_PATH = path.resolve(__dirname, '../src/buildInfo.ts');

function generateBuildInfo() {
  try {
    const pkg = JSON.parse(fs.readFileSync(PACKAGE_JSON_PATH, 'utf8'));
    
    const buildInfo = {
      appName: pkg.metadata?.name || pkg.name,
      version: pkg.version || 'ERROR',
      environment: pkg.appConfig?.environment || 'Development',
      ProjectUrl: pkg.appConfig?.ProjectUrl || 'None',
      buildTime: new Date().toLocaleString('zh-CN', { hour12: false}),
      description: pkg.description
    };

    const fileContent = `// Auto-generated by scripts/generate-build-info.js
// Do not edit this file manually

export const BUILD_INFO = ${JSON.stringify(buildInfo, null, 2)};
`;

    fs.writeFileSync(OUTPUT_PATH, fileContent, 'utf8');
    console.log('✅ Generated src/buildInfo.ts');
    console.log(`   Version: ${buildInfo.version}`);
    console.log(`   Env:     ${buildInfo.environment}`);

  } catch (error) {
    console.error('❌ Error generating build info:', error);
    process.exit(1);
  }
}

generateBuildInfo();